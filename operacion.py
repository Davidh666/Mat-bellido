# Definimos dos números complejos
# Formato: complex(real, imaginario) o directamente con 'j'
z1 = 4 + 5j
z2 = 2 + 3j

# Operaciones
suma = z1 + z2
resta = z1 - z2
multiplicacion = z1 * z2
division = z1 / z2

# Resultados
print("--- Operaciones con Números Complejos ---")
print(f"Número 1: {z1}")
print(f"Número 2: {z2}")
print("-" * 30)
print(f"Suma:           {suma}")
print(f"Resta:          {resta}")
print(f"Multiplicación: {multiplicacion}")
print(f"División:       {division}")